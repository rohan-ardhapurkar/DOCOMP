package com.docomp.beans;


/**
 * @author rohan
 * This class is bean class of Patient
 */
public class PatientBean {
	private int patientId = 0;
	private String patientName = null;
	private String patientLastName = null;
	private String patientGender = null;
	private String patientPhone = null;
	private String patientAddress = null;
	private String patientHeight = null;
	private String patientWeight = null;
	private String patientAge = null;
	/**
	 * @return the patientId
	 */
	public int getPatientId() {
		return patientId;
	}
	/**
	 * @param patientId the patientId to set
	 */
	public void setPatientId(int patientId) {
		this.patientId = patientId;
	}
	/**
	 * @return the patientName
	 */
	public String getPatientName() {
		return patientName;
	}
	/**
	 * @param patientName the patientName to set
	 */
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	/**
	 * @return the patientLastName
	 */
	public String getPatientLastName() {
		return patientLastName;
	}
	/**
	 * @param patientLastName the patientLastName to set
	 */
	public void setPatientLastName(String patientLastName) {
		this.patientLastName = patientLastName;
	}
	/**
	 * @return the patientGender
	 */
	public String getPatientGender() {
		return patientGender;
	}
	/**
	 * @param patientGender the patientGender to set
	 */
	public void setPatientGender(String patientGender) {
		this.patientGender = patientGender;
	}
	/**
	 * @return the patientPhone
	 */
	public String getPatientPhone() {
		return patientPhone;
	}
	/**
	 * @param patientPhone the patientPhone to set
	 */
	public void setPatientPhone(String patientPhone) {
		this.patientPhone = patientPhone;
	}
	/**
	 * @return the patientAddress
	 */
	public String getPatientAddress() {
		return patientAddress;
	}
	/**
	 * @param patientAddress the patientAddress to set
	 */
	public void setPatientAddress(String patientAddress) {
		this.patientAddress = patientAddress;
	}
	/**
	 * @return the patientHeight
	 */
	public String getPatientHeight() {
		return patientHeight;
	}
	/**
	 * @param patientHeight the patientHeight to set
	 */
	public void setPatientHeight(String patientHeight) {
		this.patientHeight = patientHeight;
	}
	/**
	 * @return the patientWeight
	 */
	public String getPatientWeight() {
		return patientWeight;
	}
	/**
	 * @param patientWeight the patientWeight to set
	 */
	public void setPatientWeight(String patientWeight) {
		this.patientWeight = patientWeight;
	}
	/**
	 * @return the patientAge
	 */
	public String getPatientAge() {
		return patientAge;
	}
	/**
	 * @param patientAge the patientAge to set
	 */
	public void setPatientAge(String patientAge) {
		this.patientAge = patientAge;
	}
	
}
